/**
 * Função do plugin que adiciona variáveis globais para cada cor
 * Utiliza o flattenColorPalette para gerar as cores do Tailwind como variáveis CSS
 */
function addVariablesForColors({ addBase, theme }) {
  let allColors = flattenColorPalette(theme("colors"));
  let newVars = Object.fromEntries(
    Object.entries(allColors).map(([key, val]) => [`--${key}`, val])
  );

  addBase({
    ":root": newVars, // Adiciona as variáveis no :root
  });
}
